    <h3>🚗 Pourquoi ce site a-t-il été créé ?</h3>
    <p>EcoRide est née d’un constat simple : <strong>trop de voitures circulent à moitié vides</strong>, alors que des millions de personnes partagent des trajets similaires chaque jour. Le but de ce site est donc de <em>mettre en relation des conducteurs et des passagers</em>, afin de remplir ces voitures et <strong>réduire le nombre total de véhicules sur la route</strong>.</p>

    <p>Ce <strong>gain de place</strong> est aussi un <strong>gain pour la planète</strong> : <em>moins d’émissions de CO₂, moins de trafic, moins de stress</em>. Pour les utilisateurs, c’est également un moyen de <strong>faire des économies</strong> sur les frais de carburant tout en <em>créant du lien social</em>.</p>

